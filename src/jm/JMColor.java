package jm;

import java.awt.Color;

public class JMColor {

	/** The default background color for any JComponent **/
	public static final Color DEFAULT_BACKGROUND = Color.WHITE;
	
	public static final Color DEFAULT_FONT_COLOR = Color.BLACK;
	
	/** The default color for any JComponent that has been disabled **/
	public static final Color DISABLED_BACKGROUND_COLOR = Color.decode("#CCCCCC");
	
	public static final Color DISABLED_FONT_COLOR = Color.DARK_GRAY;
	
	/** The default color of a JButton when the user hovers over it. **/
	public static final Color HOVER_COLOR = Color.decode("#D9EBF9");

	/** The default color of a border for any JButton when the user hovers over it **/
	public static final Color HOVER_BORDER_COLOR = Color.decode("#0078D7");
	
	/** The color of a JButton when a user presses on it **/
	public static final Color PRESS_COLOR = Color.decode("#BCDCF4");
	
	/** The default color of a border for any JComponent **/
	public static final Color DEFAULT_BORDER_COLOR = Color.decode("#ABADB3");

	/** The default color for the border of any disabled JComponent **/
	public static final Color DISABLED_BORDER_COLOR = Color.decode("#BFBFBF");
	
	public static final Color DEFAULT_FOREGROUND = Color.BLACK;
	
	public static final Color DISABLED_FOREGROUND_COLOR = Color.decode("#838383");
}
